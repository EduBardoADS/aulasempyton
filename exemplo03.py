#Exemplo 03 - Operadores
#Operações aritiméticas basicas
#Entrada de dados
n1 = int(input('Digite um número: '))
n2 = int(input('Digite outro número: ')) 
#Processamento e saída
print("Soma = ", (n1+n2))
print("Subbtração = ", (n1-n2))
print("Divisão = ", (n1/n2))
print("Parte inteira Divisão = ", (n1//n2))
print("Multiplicação = ", (n1*n2))
print("Resto da divisão = ", (n1%n2))
print("Exponenciação = ", (n1**3))
print("Exponenciação = ", pow(n1,3))


